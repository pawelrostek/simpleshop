var cbpAnimatedHeader = (function() {

	var docElem = document.documentElement,
		  headerNavBar = document.querySelector( '.navbar-default' ),
      headerNavIntro = document.querySelector( '#continer-intro-text' ),
      navPageUpper = document.querySelector( '.page-upper' ),
		  didScroll = false,
      didScrollMob = false,
      didScrollNav = false,
		  changeHeaderNavOn = 371;
      changeHeaderNavMobOn = 265;


	function init() {
		window.addEventListener( 'scroll', function( event ) {
			
      console.log($(window).scrollTop());

      if( !didScrollMob ) {
        didScrollMob = true;
        hideNavBarMob();
      }

      if( !didScroll ) {
				didScroll = true;
        minimalizeNavBar();
				// setTimeout( minimalizeNavBar, 20 );
			}

      if( !didScrollNav ) {
        didScrollNav = true;
        hideNavTop();
      }

		}, false );
	}

  function hideNavBarMob() {
    var sy = scrollY();
    if ( sy >= changeHeaderNavMobOn ) {
      classie.add( headerNavIntro, 'navbar-shrink-mob' );
    } else {
      classie.remove( headerNavIntro, 'navbar-shrink-mob' );
    }
    didScrollMob = false;
  }

	function minimalizeNavBar() {
		var sy = scrollY();
		if ( sy >= changeHeaderNavOn ) {
      classie.add( headerNavBar, 'navbar-shrink' );
      classie.add( headerNavIntro, 'navbar-shrink' );
		} else {
      classie.remove( headerNavBar, 'navbar-shrink' );
      classie.remove( headerNavIntro, 'navbar-shrink' );
		}
		didScroll = false;
	}


  function hideNavTop() {
    var sy = scrollY();
    if ( sy >= changeHeaderNavMobOn ) {
      classie.add( navPageUpper, 'page-upper-visible' );
    } else {
      classie.remove( navPageUpper, 'page-upper-visible' );
    }
    didScrollNav = false;
  }

	function scrollY() {
		return window.pageYOffset || docElem.scrollTop;
	}

	init();

})();






